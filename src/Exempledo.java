public class Exempledo {
    public static void main(String[] args) {

        int i = 0;
        do {
            System.out.println(i);
            i = i + 4;


        } while (i < 10);

            /*
           IMPORTANT Do while vs While
            While(x=0) repeats what is inside forever as long as x stays =0
            Do while does the action first, and only repeats if the condition is true.





             */
    }
}